from turtle import Turtle


class Paddle(Turtle):
    def __init__(self, x_co, y_co,stretch_len):
        super().__init__()
        self.shape("square")
        self.color("white")
        self.penup()
        self.shapesize(stretch_wid=5, stretch_len=stretch_len)
        self.goto(x_co, y_co)

    def go_up(self):
        new_y = self.ycor() + 20
        self.goto(self.xcor(), new_y)

    def go_down(self):
        new_y = self.ycor() - 20
        self.goto(self.xcor(), new_y)
